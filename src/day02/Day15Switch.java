package day02;

public class Day15Switch {

	public static void main(String[] args) {
		
		int day = 2;
		
		switch (day) {
		
		case 1 : 
		
		System.out.println("Monday");
		
		case 2 : 
			
			System.out.println("Tuesday");
			break;
		case 3 : 
			
			System.out.println("wednesday");
			
		default : 
			
			System.out.println("Invalid day");
		
		}
		
		System.out.println("Done");
	}

}
